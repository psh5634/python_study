# 연산자 

print(5 + 2)
print(5 - 2)
print(5 * 2)
print(5 / 2)
print(5 % 2)
print(5 // 2)
print(5 ** 2)  # 산술 연산자 

print(5 > 2)
print(5 >= 2)
print(5 < 2)
print(5 <= 2)
print(5 == 2)
print(5 != 2)  # 비교 연산자

print(3 < 5 and 7 < 5)
print(3 < 5 or 7 < 5)
print(not 3 < 5)  # 논리 연산자 

print('c' in 'cat')
print('c' not in 'cat')  # 멤버 연산자 


